#include <string.h>
#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	i;

	while (*s == ' ' || *s == '\n' || *s == '\t')
		++s;
	i = 0;
	while (s[i])
		++i;
	while (i && (s[i - 1] == ' ' || s[i - 1] == '\n' || s[i - 1] == '\t'))
		--i;
	return (ft_strsub(s, 0, i));
}
